print("Tipe-tipe data python")
#tipe data
#1.angka satuan (integer)
data_integer = 1
print("data", data_integer)
print("-bertipe :", type(data_integer))

#2. Desimal atau float
#angka dengan koma
data_float = 3.4
print("data:", data_float)
print("-bertipe:", type(data_float))

#3. String (kumpulan karakter)
data_string = "starbak"
print("data:", data_string)
print("-bertipe:", type(data_string))

#4. Biner true/false (boolean)
data_boolean = True
print("data:", data_boolean)
print("-bertipe:", type(data_boolean))

#5. Khusus
#5.1. Bilangan kompleks
data_kompleks = complex(5,6)
#5 adalah bilangan real, 6 bilangan imajiner
print("data:", data_kompleks)
print("-bertipe:", type(data_kompleks))